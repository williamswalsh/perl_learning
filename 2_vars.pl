#!/usr/bin/env perl

# Scalar
my $animal = "koala";
my $answer = 42;

print "My animal is: $animal";


# Perl uses commas to concatenate strings
print "\nThe square of my answer: $answer is " , $answer * $answer , ".\n";



# $_ -> Default variable
# Used as the default argument to a number of functions
# print prints the contents of $_ by default
print;